#Character 1:-

#Primitive Datatypes:
name = "Eugeo"  #string
age = 17        #int
level = 5
hp = 100.0      #float
money = 1000.0
isAlive = True  #bool

#Non-Primitive Datatypes:
inventory = ["Blue Rose Sword", "Potion", "Elixer", "Food"]    #list  (mutable)
position = (10,12)   #tuple (immutable)
char_stats = {"Attack":7, "Defense":6, "Agility":8, "Magic":10}  #dict key-value pair
rare_items = {"Max Potion", "Master Key", "Max Elixir"}   #sets

#Printing the Character Profile:
print(f"WELCOME Player | {name}")
print("Your Profile:")
print(f"Name: {name} || Age: {age} || Level: {level} || HP: {hp} || Money: {money}")
print(f"Your Inventory: {inventory}")
print(f"Your Stats: {char_stats}")
print(f"Rare Items: {rare_items}\n")


